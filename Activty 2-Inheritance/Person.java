public class Person {
    protected String Name;
    protected String Address;
    
    //Setters
    public void setName(String name){
        this.Name = name;
    }
    public void setAddress(String address){
        this.Address = address;
    }
    
    //Getters
    public String getName(){
        return Name;
    }
    public String getAddress(){
        return Address;
    }
}
